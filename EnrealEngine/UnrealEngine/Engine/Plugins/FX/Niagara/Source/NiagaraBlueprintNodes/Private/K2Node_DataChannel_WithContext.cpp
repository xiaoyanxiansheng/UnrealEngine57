void UK2Node_DataChannelAccessContextOperation::GetMenuActions(FBlueprintActionDatabaseRegistrar& ActionRegistrar) const
{
	const UClass* ActionKey = GetClass();
	if (!ActionRegistrar.IsOpenForRegistration(GetClass()))
	{
		return;
	}

	//Add a menu item for all subtypes of FNDCAccessContextBase.
	TArray<UScriptStruct*> ContextStructs;
	for (TObjectIterator<UScriptStruct> It; It; ++It)
	{
		UScriptStruct* Struct = *It;
		if (Struct->IsChildOf(FNDCAccessContextBase::StaticStruct()))
		{
			auto PostNodeSpawn = UBlueprintNodeSpawner::FCustomizeNodeDelegate::CreateLambda([Struct](UEdGraphNode* NewNode, bool bIsTemplate)
			{
				if(UK2Node_DataChannelAccessContextOperation* TypedNode = Cast<UK2Node_DataChannelAccessContextOperation>(NewNode))
				{
					TypedNode->ContextStruct = Struct;
				}
			});

			UBlueprintNodeSpawner* NodeSpawner = UBlueprintNodeSpawner::Create(GetClass(), nullptr, PostNodeSpawn);
			NodeSpawner->DefaultMenuSignature.MenuName = GetNodeTitleForContext(Struct);
			NodeSpawner->DefaultMenuSignature.Category = LOCTEXT("Niagara Data Channels Menu Category", "Niagara Data Channel");
			NodeSpawner->DefaultMenuSignature.Tooltip = GetTooltipTextForContext(Struct);

			ActionRegistrar.AddBlueprintAction(ActionKey, NodeSpawner);
		}
	}
}

bool UK2Node_DataChannelAccessContextOperation::IsActionFilteredOut(class FBlueprintActionFilter const& Filter)
{	
	for(UEdGraphPin* Pin : Filter.Context.Pins)
	{
		if(UK2Node_DataChannelAccessContextOperation* ContextNode = Cast<UK2Node_DataChannelAccessContextOperation>(Pin->GetOwningNode()))
		{
			if(ContextNode->ContextStruct != ContextStruct)
			{
				return true;
			}
		}
	}

	return false;
}

