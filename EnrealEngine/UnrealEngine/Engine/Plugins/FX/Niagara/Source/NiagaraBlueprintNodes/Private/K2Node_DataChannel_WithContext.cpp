void UK2Node_DataChannelAccessContextOperation::ExpandNodeOptionalPins(class FKismetCompilerContext& CompilerContext, UEdGraph* SourceGraph, UEdGraphPin* AccessContextSourcePin, UEdGraphPin* CurrThen, FName PerPropertyFuncName)
{
	check(CurrThen);

	//For all of our optional property pins, create shunt the pin to a new function call to perform the operation on that property only.
	//TODO: Can likely consolidate this into a single custom thunk function in future.
	TArray<UEdGraphPin*> ToRemove;
	const UEdGraphSchema_K2* Schema = CastChecked<UEdGraphSchema_K2>(GetSchema());
	for (UEdGraphPin* SrcPin : Pins)
	{
		bool bSkip = SrcPin->PinType.PinCategory == UEdGraphSchema_K2::PC_Exec || SrcPin == AccessContextSourcePin || (SrcPin->Direction == EGPD_Output && SrcPin->LinkedTo.Num() == 0);
		if (bSkip)
		{
			continue;
		}
		//For each visible pin we create a function call node to set that single property on our new context.
		if (FOptionalPinFromProperty* OptionalPin = ShowPinForProperties.FindByPredicate([&](const FOptionalPinFromProperty& CheckPin) { return CheckPin.PropertyName == SrcPin->PinName && CheckPin.bShowPin == true; }))
		{
			ToRemove.Add(SrcPin);
			//If we're an optional pin we should remove from the main function body.
			FProperty* Property = ContextStruct->FindPropertyByName(OptionalPin->PropertyName);
			{
				UK2Node_CallFunction* PropertyFuncCallNode = CompilerContext.SpawnIntermediateNode<UK2Node_CallFunction>(this, SourceGraph);
				FName PropertyFuncName = PerPropertyFuncName;
				PropertyFuncCallNode->SetFromFunction(UNiagaraDataChannelLibrary::StaticClass()->FindFunctionByName(PropertyFuncName));
				PropertyFuncCallNode->AllocateDefaultPins();

				UEdGraphPin* PropertyAccessContextPin = PropertyFuncCallNode->FindPinChecked(TEXT("AccessContext"));
				UEdGraphPin* PropertyContextStructPin = PropertyFuncCallNode->FindPinChecked(TEXT("ContextStruct"));
				UEdGraphPin* PropertyNamePin = PropertyFuncCallNode->FindPinChecked(TEXT("PropertyName"));
				UEdGraphPin* PropertyValuePin = PropertyFuncCallNode->FindPinChecked(TEXT("Value"));

				Schema->ConvertPropertyToPinType(Property, PropertyValuePin->PinType);

				//Needs to be a reference to be consumed as a wildcard by our custom thunk.
				//AutoCreateRefTerm on the function meta will ensure unconnected pins are auto generated a reference property.
				PropertyValuePin->PinType.bIsReference = true;

				//Connect exec and then pins				
				CompilerContext.MovePinLinksToIntermediate(*CurrThen, *PropertyFuncCallNode->GetThenPin());
				Schema->TryCreateConnection(CurrThen, PropertyFuncCallNode->GetExecPin());
				CurrThen = PropertyFuncCallNode->GetThenPin();

				Schema->TryCreateConnection(PropertyAccessContextPin, AccessContextSourcePin);
				
				PropertyContextStructPin->DefaultObject = ContextStruct;
				
				PropertyNamePin->DefaultValue = OptionalPin->PropertyName.ToString();

				if (SrcPin->LinkedTo.Num() > 0)
				{
					CompilerContext.MovePinLinksToIntermediate(*SrcPin, *PropertyValuePin);
				}
				else if (SrcPin->Direction == EGPD_Input)
				{
					//If we're an input that's unconnected, setup our default value.
 					PropertyValuePin->DefaultValue = SrcPin->DefaultValue;
 					PropertyValuePin->DefaultObject = SrcPin->DefaultObject;
 					PropertyValuePin->DefaultTextValue = SrcPin->DefaultTextValue;
				}
			}
		}
	}

	//Remove our optional property pins from the main function call.
	for(UEdGraphPin* SrcPin : ToRemove)
	{
		RemovePin(SrcPin);
	}
}

