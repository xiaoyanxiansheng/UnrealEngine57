//Optional Pin Manager that will by default only show transient input properties for an NDC Access Context.
//Used for setting values on an existing access context and so transient input pins are the most likely things we'll want to set here.
struct FK2Node_SetMembersInNDCAccessContext_OptionalPinManager : public FK2Node_MakeNDCAccessContextInst_OptionalPinManager
{
	FK2Node_SetMembersInNDCAccessContext_OptionalPinManager(const uint8* InSampleStructMemory, UBlueprint* BP) : FK2Node_MakeNDCAccessContextInst_OptionalPinManager(InSampleStructMemory, BP)
	{}

	//~ Begin FOptionalPinsUpdater Interface
	virtual void GetRecordDefaults(FProperty* TestProperty, FOptionalPinFromProperty& Record) const override
	{
		Record.bCanToggleVisibility = true;
		Record.bShowPin = TestProperty->HasMetaData(TEXT("NDCAccessContextInput")) && TestProperty->HasAnyPropertyFlags(CPF_Transient);//For the set fields node we default to any transient inputs.
	}

	virtual void CustomizePinData(UEdGraphPin* Pin, FName SourcePropertyName, int32 ArrayIndex, FProperty* Property) const override
	{
		FOptionalPinManager::CustomizePinData(Pin, SourcePropertyName, ArrayIndex, Property);
	}
};

