void UK2Node_DataChannelAccessContext_GetMembers::ExpandNode(class FKismetCompilerContext& CompilerContext, UEdGraph* SourceGraph)
{
	Super::ExpandNode(CompilerContext, SourceGraph);

	if (!ContextStruct)
	{
		CompilerContext.MessageLog.Error(*LOCTEXT("NoValidAccessContextStruct", "Access Context Struct is null! - @@").ToString(), this);
		return;
	}

	UEdGraphPin* OrigAccessContextPin = FindPinChecked(TEXT("AccessContext"));
	UEdGraphPin* AccessContextSourcePin = OrigAccessContextPin->LinkedTo.Num() > 0 ? OrigAccessContextPin->LinkedTo[0] : nullptr;
	if (!AccessContextSourcePin)
	{
		CompilerContext.MessageLog.Error(*LOCTEXT("NoValidAccessContext", "Access Context input must be connected. - @@").ToString(), this);
		return;
	}

	//Inject a noop exec pin just so we have a single exec point to go into our other expansion pins.
	UK2Node_ExecutionSequence* NoopNode = CompilerContext.SpawnIntermediateNode<UK2Node_ExecutionSequence>(this, SourceGraph);
	NoopNode->AllocateDefaultPins();
	CompilerContext.MovePinLinksToIntermediate(*GetExecPin(), *NoopNode->GetExecPin());
	CompilerContext.MovePinLinksToIntermediate(*GetThenPin(), *NoopNode->GetThenPinGivenIndex(0));

	ExpandNodeOptionalPins(CompilerContext, SourceGraph, AccessContextSourcePin, NoopNode->GetThenPinGivenIndex(0), GET_FUNCTION_NAME_CHECKED(UNiagaraDataChannelLibrary, GetSinglePropertyInNDCAccessContextInstance));

	//This node is fully expanded way so clear out any remaining links
	BreakAllNodeLinks();
}

