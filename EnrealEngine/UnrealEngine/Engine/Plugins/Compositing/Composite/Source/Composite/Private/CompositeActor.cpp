void ACompositeActor::SetActive(bool bInActive)
{
	bIsActive = bInActive;

	PropagateStateChange(IsRendering());

	// Deactivate all other composite actors
	const UWorld* World = GetWorld();

	if (bIsActive && IsValid(World))
	{
		for (TActorIterator<ACompositeActor> It(World); It; ++It)
		{
			if (*It != this)
			{
				It->bIsActive = false;
				It->PropagateStateChange(false);
			}
		}
	}

	// Update the view projection matrix sent to the material parameter collection when changing which actor is active.
	if (bIsActive && IsValid(ViewProjectionComponent))
	{
		ViewProjectionComponent->ForceUpdate();
	}
}

