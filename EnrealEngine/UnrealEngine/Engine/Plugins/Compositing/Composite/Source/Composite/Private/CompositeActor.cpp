ACompositeActor::ACompositeActor(const FObjectInitializer& ObjectInitializer)
	: Super(ObjectInitializer)
	, ViewProjectionComponent{}
	, bIsActive{true}
	, bIsEnabled{true}
// TODO: Can we find the current viewport render resolution instead?
// View rects are only accessible from the render thread, but maybe there is an alternative.
// This isn't needed by custom render passes, only regular scene capture render targets...
	, RenderResolution{1920, 1080}
	, bEnableScreenSpaceReflections{true}
	, bOverridesViewUserFlags{true}
	, ViewUserFlags{1}
	, MainRenderOutput{ECompositeMainRenderOutputMode::Default}
	, AllowedViewModes{ECompositeAllowedViewModes::Default}
	, bIsModifyingAProperty{false}
{
	PrimaryActorTick.bCanEverTick = true;
	PrimaryActorTick.bTickEvenWhenPaused = true;
	PrimaryActorTick.TickGroup = TG_PostUpdateWork;
	PrimaryActorTick.bStartWithTickEnabled = true;
	bAllowTickBeforeBeginPlay = true;

	SetActorEnableCollision(false);

	RootComponent = CreateDefaultSubobject<USceneComponent>(USceneComponent::GetDefaultSceneRootVariableName());

	ViewProjectionComponent = CreateDefaultSubobject<UCompositeViewProjectionComponent>(TEXT("CameraViewProjectionComponent"));
	AddInstanceComponent(ViewProjectionComponent);
}

ACompositeActor::~ACompositeActor() = default;

void ACompositeActor::PostLoad()
{
	Super::PostLoad();

#if WITH_EDITORONLY_DATA
	/**
	* Refactored scene capture management.
	* Instead of polluting the actor with old data structures, we remove old components and rely on their recreation.
	*/
	const bool bHandleSceneCaptureRefactor = GetLinkerCustomVersion(FFortniteMainBranchObjectVersion::GUID) < FFortniteMainBranchObjectVersion::CompositeActorSceneCaptureRefactor;
	const bool bHandleDerivedSceneCaptureTypes = GetLinkerCustomVersion(FUE5ReleaseStreamObjectVersion::GUID) < FUE5ReleaseStreamObjectVersion::CompositePluginDerivedSceneCaptures;

	if (bHandleSceneCaptureRefactor || bHandleDerivedSceneCaptureTypes)
	{
		TArray<UActorComponent*> ActorComponents;
		GetComponents(ActorComponents);

		for (auto It = ActorComponents.CreateIterator(); It; ++It)
		{
			if (USceneCaptureComponent2D* CaptureComponent = Cast<USceneCaptureComponent2D>(*It))
			{
				bool bDestroyComponent = false;

				if (bHandleSceneCaptureRefactor && CaptureComponent->IsA<UCompositeShadowReflectionCatcherComponent>())
				{
					bDestroyComponent = true;
				}

				if (bHandleDerivedSceneCaptureTypes && (CaptureComponent->GetClass() == USceneCaptureComponent2D::StaticClass()))
				{
					bDestroyComponent = true;
				}

				if (bDestroyComponent)
				{
					RemoveInstanceComponent(CaptureComponent);
					CaptureComponent->UnregisterComponent();
					CaptureComponent->DestroyComponent();
				}
			}
		}
	}
#endif // WITH_EDITORONLY_DATA
}

void ACompositeActor::PostRegisterAllComponents()
{
	using namespace UE::Composite::Private;

	Super::PostRegisterAllComponents();

#if WITH_EDITOR
	if (IsTemplate() || bIsModifyingAProperty)
	{
		return;
	}
#endif

	if (IsRendering())
	{
		PropagateStateChange(true);
	}
}

void ACompositeActor::UnregisterAllComponents(bool bForReregister)
{
	using namespace UE::Composite::Private;

	Super::UnregisterAllComponents(bForReregister);

	if (bForReregister || bIsModifyingAProperty)
	{
		return;
	}
	
	// State removal is done without any conditions to prevent stale state
	PropagateStateChange(false);
}

