void ACompositeActor::PreDuplicateFromRoot(FObjectDuplicationParameters& DupParams)
{
	Super::PreDuplicateFromRoot(DupParams);

	/**
	 * Whether DupParams.DuplicateMode == EDuplicateMode::PIE or otherwise,
	 * we release transient scene capture render targets (which are not instanced properties)
	 * to prevent their duplication. Allocation must instead come from the pool.
	 * 
	 * This is necessary since scene capture's TextureTarget property is not instanced.
	 */
	TInlineComponentArray<USceneCaptureComponent2D*> SceneCaptureComponents(this);
	for (USceneCaptureComponent2D* CaptureComponent : SceneCaptureComponents)
	{
		FCompositeRenderTargetPool& Pool = FCompositeRenderTargetPool::Get();

		if (Pool.IsAssignedTarget(CaptureComponent->TextureTarget))
		{
			Pool.ReleaseTarget(CaptureComponent->TextureTarget);
		}
	}
}

void ACompositeActor::PreEditChange(FProperty* PropertyAboutToChange)
{
	TOptional<TGuardValue<bool>> ModifyingPropertyGuard;
	if (!IsActorBeingDestroyed())
	{
		if ((GEditor && GEditor->bIsSimulatingInEditor && GetWorld() != nullptr) || ReregisterComponentsWhenModified())
		{
			if (PropertyAboutToChange)
			{
				ModifyingPropertyGuard.Emplace(bIsModifyingAProperty, true);
			}
		}
	}

	Super::PreEditChange(PropertyAboutToChange);

	if (PropertyAboutToChange && PropertyAboutToChange->GetFName() == GET_MEMBER_NAME_CHECKED(ThisClass, CompositeLayers))
	{
		PreEditCompositeLayers = CompositeLayers;
	}
}

void ACompositeActor::PostEditChangeChainProperty(struct FPropertyChangedChainEvent& PropertyChangedChainEvent)
{
	TGuardValue<bool> ModifyingPropertyGuard{ bIsModifyingAProperty, true };

	Super::PostEditChangeChainProperty(PropertyChangedChainEvent);

	const FName PropertyName = PropertyChangedChainEvent.GetPropertyName();

	if (PropertyName == GET_MEMBER_NAME_CHECKED(ThisClass, bIsActive))
	{
		SetActive(bIsActive);
	}
	else if (PropertyName == GET_MEMBER_NAME_CHECKED(ThisClass, bIsEnabled))
	{
		SetEnabled(bIsEnabled);
	}
	else if (PropertyName == GET_MEMBER_NAME_CHECKED(ThisClass, CompositeLayers))
	{
		// Verify that there isn't another container property node further down in the chain (so we don't respond to array change events from a different array!)
		bool bWasNestedContainerChanged = false;
		FEditPropertyChain::TDoubleLinkedListNode* Node = PropertyChangedChainEvent.PropertyChain.GetActiveNode()->GetNextNode();
		while (!bWasNestedContainerChanged && Node && Node->GetValue())
		{
			bWasNestedContainerChanged = Node->GetValue()->IsA<FArrayProperty>() || Node->GetValue()->IsA<FSetProperty>() || Node->GetValue()->IsA<FMapProperty>();
			Node = Node->GetNextNode();
		}

		if (!bWasNestedContainerChanged)
		{
			const int32 ArrayIndex = PropertyChangedChainEvent.GetArrayIndex(PropertyName.ToString());

			int32 MainRenderIndex = INDEX_NONE;
			
			for (int32 Index =0; Index < CompositeLayers.Num(); ++Index)
			{
				const TObjectPtr<UCompositeLayerBase>& Layer = CompositeLayers[Index];

				if (!IsValid(Layer) || !Layer->IsEnabled())
				{
					continue;
				}

				if (Layer->IsA(UCompositeLayerMainRender::StaticClass()))
				{
					MainRenderIndex = Index;
				}
			}

			for (UCompositeLayerBase* PreEditCompositeLayer : PreEditCompositeLayers)
			{
				// Note: We don't use IsValid(..) since destruction should proceed even if the actor is pending kill.
				if (PreEditCompositeLayer != nullptr)
				{
					if (!CompositeLayers.Contains(PreEditCompositeLayer))
					{
						PreEditCompositeLayer->OnRemoved(GetWorld());
					}
				}
			}

			PreEditCompositeLayers.Reset();

			if (PropertyChangedChainEvent.PropertyChain.Num() <= 1)
			{
				for (int32 Index = 0; Index < CompositeLayers.Num(); ++Index)
				{
					if (UCompositeLayerPlate* PlateLayer = Cast<UCompositeLayerPlate>(CompositeLayers[Index]))
					{
						// For user convenience when dragging layers, we disable the composite mesh pipeline if the plate layer runs after the main render.
						// TODO: Invert condition if/when traversal is done in the regular indexed order in the new UI.
						PlateLayer->SetPlateMode(Index > MainRenderIndex ? ECompositePlateMode::CompositeMesh : ECompositePlateMode::Texture);
					}
				}
			}
		}
	}
	else if (PropertyName == GET_MEMBER_NAME_CHECKED(ThisClass, Camera))
	{
		if (IsValid(ViewProjectionComponent))
		{
			ViewProjectionComponent->SetTargetComponent(Camera);
		}
	}
}

void ACompositeActor::PreEditUndo()
{
	Super::PreEditUndo();

	PreEditCompositeLayers = CompositeLayers;
}

void ACompositeActor::PostEditUndo()
{
	for (UCompositeLayerBase* PreEditCompositeLayer : PreEditCompositeLayers)
	{
		// Note: We don't use IsValid(..) since destruction should proceed even if the actor is pending kill.
		if (PreEditCompositeLayer != nullptr)
		{
			if (!CompositeLayers.Contains(PreEditCompositeLayer))
			{
				PreEditCompositeLayer->OnRemoved(GetWorld());
			}
		}
	}

	PreEditCompositeLayers.Reset();

	UWorld* World = GetWorld();

	// Deactivate the current actor if any other actor is already active
	// This can a occur when a previously active actor deletion is undone.
	if (IsValid(World) && IsActive())
	{
		for (TActorIterator<ACompositeActor> It(World); It; ++It)
		{
			ACompositeActor* OtherActor = *It;
			if ((OtherActor != this) && IsValid(OtherActor) && OtherActor->IsActive())
			{
				SetActive(false);
				break;
			}
		}
	}

	Super::PostEditUndo();
}

void ACompositeActor::PostTransacted(const FTransactionObjectEvent& TransactionEvent)
{
	Super::PostTransacted(TransactionEvent);

	if (TransactionEvent.GetEventType() == ETransactionObjectEventType::UndoRedo)
	{
		const FName RenderResolutionName = GET_MEMBER_NAME_CHECKED(ThisClass, RenderResolution);
		if (TransactionEvent.GetChangedProperties().Contains(RenderResolutionName))
		{
			/**
			* When a resolution change is undone, the previous render targets are deserialized & replaced.
			* This leaves previous assignments invalid so we make sure to return them to the pool.
			*/
			TInlineComponentArray<USceneCaptureComponent2D*> SceneCaptureComponents(this);
			for (USceneCaptureComponent2D* CaptureComponent : SceneCaptureComponents)
			{
				FCompositeRenderTargetPool::Get().ReleaseAssigneeTargets(CaptureComponent);
			}
		}
	}
}

